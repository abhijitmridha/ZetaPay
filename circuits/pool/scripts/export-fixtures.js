import fs from 'fs';
import path from 'path';

const buildDir = path.join(process.cwd(), 'circuits/pool/build');
const inputsDir = path.join(process.cwd(), 'circuits/pool/inputs');
const outputPath = path.join(process.cwd(), 'contracts/zetapay-pool/src/fixtures.rs');

const proofPath = path.join(buildDir, 'withdraw_proof.json');
const publicPath = path.join(buildDir, 'withdraw_public.json');
const vkPath = path.join(buildDir, 'withdraw_verification_key.json');
const treePath = path.join(inputsDir, 'tree.json');

function readJson(filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Missing file: ${filePath}`);
  }

  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function normalizeHex(value) {
  let hex = BigInt(value).toString(16);

  if (hex.length % 2 !== 0) {
    hex = `0${hex}`;
  }

  return hex.padStart(64, '0');
}

function frBytes(value) {
  return Array.from(Buffer.from(normalizeHex(value), 'hex'));
}

function g1Bytes(point) {
  return [...frBytes(point[0]), ...frBytes(point[1])];
}

function g2Bytes(point) {
  return [
    ...frBytes(point[0][1]),
    ...frBytes(point[0][0]),
    ...frBytes(point[1][1]),
    ...frBytes(point[1][0]),
  ];
}

function rustArray(name, bytes) {
  return `pub const ${name}: [u8; ${bytes.length}] = [${bytes.join(', ')}];`;
}

function rustNestedArray(name, arrays, innerLength) {
  const inner = arrays.map((bytes) => `[${bytes.join(', ')}]`).join(',\n    ');

  return `pub const ${name}: [[u8; ${innerLength}]; ${arrays.length}] = [\n    ${inner}\n];`;
}

const proof = readJson(proofPath);
const publicSignals = readJson(publicPath);
const vk = readJson(vkPath);
const tree = readJson(treePath);

const commitment = tree.commitments?.find((value) => BigInt(value || '0') !== BigInt(0));

if (!commitment) {
  throw new Error('tree.json must contain at least one nonzero commitment');
}

const contents = `// Auto generated by circuits/pool/scripts/export-fixtures.js

${rustArray('REAL_COMMITMENT', frBytes(commitment))}

${rustArray('REAL_PROOF_A', g1Bytes(proof.pi_a))}
${rustArray('REAL_PROOF_B', g2Bytes(proof.pi_b))}
${rustArray('REAL_PROOF_C', g1Bytes(proof.pi_c))}

${rustArray('REAL_VK_ALPHA', g1Bytes(vk.vk_alpha_1))}
${rustArray('REAL_VK_BETA', g2Bytes(vk.vk_beta_2))}
${rustArray('REAL_VK_GAMMA', g2Bytes(vk.vk_gamma_2))}
${rustArray('REAL_VK_DELTA', g2Bytes(vk.vk_delta_2))}
${rustNestedArray('REAL_VK_IC', vk.IC.map(g1Bytes), 64)}

${rustNestedArray('REAL_SIGNALS', publicSignals.map(frBytes), 32)}
`;

fs.writeFileSync(outputPath, contents);

console.log('Exported pool Rust fixtures');
console.log(outputPath);
